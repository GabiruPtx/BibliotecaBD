    private void btnAvançarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAvançarActionPerformed
        
        String email = txtEmail.getText();
        String matricula = txtMatricula.getText();
        String novaSenha = new String(txtNovaSenha.getPassword());
        String confirmaSenha = new String(txtConfirmarSenha.getPassword());
        usuárioDAO dao = new usuárioDAO();
    
    if (novaSenha.equals(confirmaSenha)){
        boolean sucesso = dao.alterarSenha(matricula, email, novaSenha);
        if (sucesso) {
            JOptionPane.showMessageDialog(this, "Senha alterada com sucesso!");
            dispose();
            TelaTipoLogin novoFrame = new TelaTipoLogin();
            novoFrame.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao alterar a senha. Verifique os dados e tente novamente.", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    } else {
        JOptionPane.showMessageDialog(this, "As senhas não coincidem!", "Erro", JOptionPane.ERROR_MESSAGE);
        txtNovaSenha.setText("");
        txtConfirmarSenha.setText("");
    }    
    }//GEN-LAST:event_btnAvançarActionPerformed

